private void showDataTable(List<CTSanPham> listKhoaHocs) {
        model.setRowCount(0);
        listKhoaHocs.forEach(s -> model.addRow(new Object[]{s.getId_CTSanPham(),
            s.getId_SanPham(), s.getId_MauSac(), s.getId_NoiThat(), s.getId_ThietKe(), s.getId_Dong_Xe(), s.getId_PhanKhucXe(),
            s.getId_DongCo(), s.getId_HopSo(), s.getTen(), s.getMa_SP(), s.getGioi_Thieu()}));
    }

    

    public void showTable(int index) {
        CTSanPham ctsp = list.get(index);
        txtID.setText(ctsp.getId_CTSanPham() + "");
        txtIDSanPham.setText(ctsp.getId_SanPham() + "");
        cbbMauSac.setSelectedIndex(ctsp.getId_MauSac()-1);
        cbbNoiThat.setSelectedIndex(ctsp.getId_NoiThat()-1);
        cbbThietKe.setSelectedIndex(ctsp.getId_ThietKe()-1);
        cbbDongXe.setSelectedIndex(ctsp.getId_Dong_Xe()-1);
        cbbPhanKhucXe.setSelectedIndex(ctsp.getId_PhanKhucXe()-1);
        cbbDongCo.setSelectedIndex( ctsp.getId_DongCo()-1);
        cbbHopSo.setSelectedIndex(ctsp.getId_HopSo()-1);
        txtMaSP.setText(ctsp.getMa_SP());
        txtTen.setText(ctsp.getTen());
        txtGioiThieu.setText(ctsp.getGioi_Thieu());
    }

    private CTSanPham getFromData() {

        int id = Integer.parseInt(txtID.getText());
        int idspham = Integer.parseInt(txtIDSanPham.getText());
        int idmsac = cbbMauSac.getSelectedIndex() +1;
        int idnthat = cbbNoiThat.getSelectedIndex() +1;
        int idtke = cbbThietKe.getSelectedIndex() +1;
        int iddxe = cbbDongXe.getSelectedIndex() +1;
        int idpkhuc = cbbPhanKhucXe.getSelectedIndex() +1;
        int iddCo = cbbDongCo.getSelectedIndex() +1;
        int idhSo = cbbHopSo.getSelectedIndex() +1;
        
        String ten = txtTen.getText();
        String masp = txtMaSP.getText();
        String gioiThieu = txtGioiThieu.getText();

        return new CTSanPham(id, idspham, idmsac , idnthat , idtke, iddxe,
                idpkhuc, iddCo, idhSo, ten, masp, gioiThieu);
    }

