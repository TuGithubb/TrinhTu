    void Next() {
        int row = tblCTSP.getSelectedRow() + 1;
        if (row == tblCTSP.getRowCount()) {
            row = 0;

        }
        tblCTSP.setRowSelectionInterval(row, row);
        showTable(row);
    }

    void Last() {
        int row = tblCTSP.getRowCount() - 1;

        tblCTSP.setRowSelectionInterval(row, row);
        showTable(row);
    }

    void First() {
        showTable(row);
        tblCTSP.setRowSelectionInterval(0, 0);

    }

    void Prev() {
        int row = tblCTSP.getSelectedRow() - 1;
        if (row < 0) {
            row = tblCTSP.getRowCount() - 1;

        }
        tblCTSP.setRowSelectionInterval(row, row);
        showTable(row);
    }
    
    private void loadComboboxMauSac() {
        String query = "SELECT Ten FROM Mau_Sac";
        try (Connection con = DBConnect.getConnection(); PreparedStatement ps = con.prepareStatement(query)) {

            ResultSet rs = ps.executeQuery();
            // show data
            String sName;
            cbbMauSac.removeAllItems();
            while (rs.next()) {
                sName = rs.getString(1);
                cbbMauSac.addItem(sName);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }

